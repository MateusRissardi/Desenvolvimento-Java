    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
        try{
            Produto umProd = cbProdutos.getItemAt(cbProdutos.getSelectedIndex());
            prodDao.editarProdutos(umProd, tfNome.getText(), Double.parseDouble(tfPreco.getText()));
            JOptionPane.showMessageDialog(null, "Produto editado com sucesso!");
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_btEditarActionPerformed

    private void btRecarregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRecarregarActionPerformed
        try{
            cbProdutos.removeAllItems();
            ArrayList<Produto> produtos = prodDao.lerProduto();
            for (Produto umProd : produtos){
                cbProdutos.addItem(umProd);
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_btRecarregarActionPerformed
     
