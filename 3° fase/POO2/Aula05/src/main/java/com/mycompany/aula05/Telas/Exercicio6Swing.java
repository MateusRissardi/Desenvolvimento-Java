    private void btContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btContinuarActionPerformed
        try{
            this.aux = tfNumeros.getText().split(";"); 
            if(this.aux.length!=5){
                JOptionPane.showMessageDialog(null, "Quantidade de numeros diferente de 5");
            }
            else{
                int cont = 0;
                for(int i = 0; i<5; i++){
                    this.num[i] = Integer.parseInt(aux[i]);
                }
                for(int i = 0; i<5; i++){
                    for(int c = 0; c<4;c++){
                        if(this.num[c]>this.num[c+1]){
                            cont = num[c];
                            num[c] = num[c+1];
                            num[c+1] = cont;
                        }
                    }
                }
                taRelatorio.setText("Numeros em ordem: ");
                for(int i = 0; i<5; i++){
                    if(i!=4){
                        taRelatorio.append(num[i] + ", ");
                    }
                    else{
                        taRelatorio.append(num[i] + "");
                    }
                }
            }
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_btContinuarActionPerformed

